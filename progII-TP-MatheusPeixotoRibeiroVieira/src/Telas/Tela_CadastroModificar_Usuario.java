    public void finalizarCadastro(){
        DAOUsuario servicos = new DAOUsuario();
        boolean prosseguir=servicos.cadastrarUsuario(usuarioNovo);
        if(prosseguir){
            //Prossegue para a próxima tela com os dados já cadastrados se não houver nenhum erro anteriormente
            Tela_opcoes abrir = new Tela_opcoes(usuarioNovo);
            abrir.setVisible(true);
            this.setVisible(false); 
        }
       
    }
    
    public void atualizar(){
        String nome = tNome.getText();
        String sobrenome = tSobrenome.getText();
        String cpf = tCPF.getText();
        String email = tEmail.getText();
        String senha = tpSenha.getText();
        double saldo = Double.parseDouble(tSaldo.getText().replaceAll(",","."));

        u.setNome(nome);
        u.setSobrenome(sobrenome);
        u.setCpf(cpf);
        u.setEmail(email);
        u.setSenha(senha);
        u.setSaldoManual(saldo);
        
        usuarioBD.atualizarUsuario(u);
    }
