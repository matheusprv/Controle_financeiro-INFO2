    public void cadastrar(){
        // Pegar dados que o usuário preencheu. Depois é verificado se o e-mail e/ou o CPF já estão cadastrados. O cadastro é realizado
        double saldo=0.0;
        if(tSaldo.getText()!=null || tSaldo.getText() !=""){
            saldo=Double.parseDouble(tSaldo.getText().replaceAll(",","."));
        }
        boolean prosseguir=true;
        usuarioNovo = new Usuario(tNome.getText(),tSobrenome.getText(),tCPF.getText(),tEmail.getText(),tpSenha.getText(),saldo);
        DAOUsuario servicos= new DAOUsuario();
        Usuario u1=servicos.verificarUsuariosCadastrados(usuarioNovo); //Verifica se já existe um usuário cadastrado com o mesmo email ou cpf     
        
        if(u1!=null){
            JOptionPane.showMessageDialog(null, "CPF ou e-mail já cadastrado", "Erro", JOptionPane.ERROR_MESSAGE);
            prosseguir=false;
        }
        else{
            //Se os dados não tiverem sido utilizados é possível criar o usuário
            //prosseguir=servicos.cadastrarUsuario(usuarioNovo);
            
            Random aleatorio = new Random();
            do{
                codigo = aleatorio.nextInt(1000000);
            }while(codigo<100000);
            EnviarEmail enviar = new EnviarEmail();
            boolean enviado = enviar.verificarEmail(usuarioNovo, codigo);
            if(enviado){
                jPanel1.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Verifique se seu email está escrito corretamente.", "Verifique o email", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    private void bVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bVerificarActionPerformed
        boolean prosseguir =false;
        if(tentativas<=3 && prosseguir==false){
            int codigoEscrito = Integer.parseInt(tVerificar.getText());
            if(codigoEscrito == codigo){
                prosseguir = true;
            }
            else{
                tVerificar.setText("");
                lblCodigoInvalido.setVisible(true);
                tentativas++;
            }
        }
        if(prosseguir){
            finalizarCadastro();
        }
        else if(tentativas>3){
            jPanel1.setVisible(false);
            JOptionPane.showMessageDialog(null, "Código expirado");
            tentativas = 0;
        }
    }//GEN-LAST:event_bVerificarActionPerformed

