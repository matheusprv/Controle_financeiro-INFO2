    private void bEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEntrarActionPerformed
        String emailCPF=jTextFieldUsuario1.getText(), senha=jPasswordField1.getText();
        Usuario verificador=null;
        if(emailCPF.contains("@")){
            verificador = new Usuario("","","",emailCPF,senha,0);
        }
        else{
            verificador = new Usuario("","",emailCPF,"",senha,0);
        }
        //System.out.println("Email: "+verificador.getEmail()+"\nCPF: "+verificador.getCpf());
        DAOUsuario servicos= new DAOUsuario();
        Usuario usuarioNoBD=servicos.verificarUsuariosCadastrados(verificador);
        
        if(usuarioNoBD==null){
            JOptionPane.showMessageDialog(null, "Usuário não cadastrado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else{
            if(senha.equals(usuarioNoBD.getSenha())){
                Tela_opcoes a= new Tela_opcoes(usuarioNoBD);
                a.setVisible(true);
                this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "Email/CPF ou senha incorretos", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_bEntrarActionPerformed

