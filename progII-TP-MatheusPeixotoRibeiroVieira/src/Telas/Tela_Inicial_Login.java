    private void bRecuperarSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRecuperarSenhaActionPerformed
        String email = tRecuperarSenha.getText();
        DAOUsuario servicos= new DAOUsuario();
        Usuario usuario = new Usuario("","","",email,"",0);
        Usuario usuarioNoBD=servicos.verificarUsuariosCadastrados(usuario);
        if(usuarioNoBD.getEmail().equals(email)){
            EnviarEmail enviar = new EnviarEmail();
            enviar.enviarSenha(usuarioNoBD);
        }
        else{
            JOptionPane.showMessageDialog(null, "O email está incorreto ou não existe!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        pRecuperarSenha.setVisible(false);
        JOptionPane.showMessageDialog(null, "Verifique o seu email!\nUma mensagem foi enviada para você!");
    }//GEN-LAST:event_bRecuperarSenhaActionPerformed

